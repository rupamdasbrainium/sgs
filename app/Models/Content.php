        'title',
        'body',
        'summary',
        'slug',
        'status',
        'deleted',
        'readable',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array<int, string>
     */
    protected $hidden = [
        'created_at',
        'updated_at',
    ];

    /**
